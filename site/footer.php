                        <h2>F<span>urniture</span></h2><br/><br/>
                        <p>Furniture refers to movable objects intended to support various human activities such as seating, eating, and sleeping. Furniture is also used to hold objects at a convenient height for work, or to store things.</p><p> Furniture can be a product of design and is considered a form of decorative art.</p><p> In addition to furniture's functional role, it can serve a symbolic or religious purpose.</p><p> It can be made from many materials, including metal, plastic, and wood. Furniture can be made using a variety of woodworking joints which often reflect the local culture.</p>
